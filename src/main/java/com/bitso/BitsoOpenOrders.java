package com.bitso;

import java.util.ArrayList;

import org.json.JSONArray;
import org.json.JSONObject;

import com.bitso.exchange.BookOrder;

public class BitsoOpenOrders {

    public ArrayList<BookOrder> list;

    public BitsoOpenOrders(JSONArray obj, BitsoBook book) {
        list = new ArrayList<BookOrder>(obj.length());
        for (int i = 0; i < obj.length(); i++) {
            JSONObject o = obj.getJSONObject(i);
            BookOrder order = Bitso.processBookOrderJSON(o.toString());
            order.book = BitsoBook.valueOf(book.toString());
            list.add(order);
        }
    }

    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("====OPEN_ORDERS====");
        if (list != null) {
            for (BookOrder o : list) {
                sb.append(o);
            }
        }
        return sb.toString();
    }
}
